function clamp01(n) { return Math.max(0, Math.min(1, n)); }
function anyMatch(list, patterns) {
  if (!patterns || !list || !list.length) return null;
  for (const p of patterns) for (const v of list) { if (typeof v === 'string' && v.includes(p)) return p; }
  return null;
}

/** Dot-bounded suffix match: `foo.lovable.app` ~ `lovable.app`, `evil-lovable.app` ✗. */
function hostHasSuffix(host, suffix) {
  return host === suffix || host.endsWith('.' + suffix);
}

/**
 * Identify a builder from the page's OWN hostname (default hosting subdomain).
 * @param {string} hostname lowercased
 * @returns {{id:string, evidence:string}|null}
 */
function matchDefaultSubdomain(hostname) {
  if (!hostname) return null;
  for (const rule of DEFAULT_SUBDOMAINS) {
    for (const suffix of rule.suffixes) {
      if (!hostHasSuffix(hostname, suffix)) continue;
      if (rule.prefix) {
        // Require the leftmost label to start with the prefix (v0-*.vercel.app).
        const label = hostname.slice(0, hostname.length - suffix.length).replace(/\.$/, '').split('.').pop() || '';
        if (!label.startsWith(rule.prefix)) continue;
      }
      return { id: rule.id, evidence: hostname };
    }
  }
  return null;
}

/** A generic vibe-coding deploy host (weak nudge). @returns matched suffix or null. */
function matchVibeHost(hostname) {
  if (!hostname) return null;
  for (const suffix of VIBE_HOSTS) if (hostHasSuffix(hostname, suffix)) return suffix;
  return null;
}
function textMatch(str, patterns) {
  if (!patterns || !str) return null;
  for (const p of patterns) { if (str.includes(p)) return p; }
  return null;
}

function scoreFingerprint(fp, h, ownHostId) {
  const m = fp.match; const hits = [];
  // The page's own default subdomain is the strongest single tell.
  const subdomain = ownHostId === fp.id;
  if (subdomain) hits.push(`subdomain ${h.hostname}`);
  const g = textMatch(h.generator, m.generator); if (g) hits.push(`generator “${g}”`);
  const host = anyMatch(h.hosts, m.hosts); if (host) hits.push(`host ${host}`);
  const data = anyMatch(h.dataAttrs, m.dataAttrs); if (data) hits.push(`attr ${data}`);
  const attr = anyMatch(h.attrs, m.attrs); if (attr) hits.push(`attr ${attr}`);
  const cls = anyMatch(h.classes, m.classes); if (cls) hits.push(`class .${cls}`);
  const path = anyMatch(h.paths, m.paths); if (path) hits.push(`path ${path}`);
  const font = textMatch(h.fonts, m.fonts); if (font) hits.push(`font ${font}`);
  const id = m.ids && (h.ids || []).find((x) => m.ids.includes(x)); if (id) hits.push(`#${id}`);
  const pw = textMatch(h.powered, m.powered); if (pw) hits.push(`“powered by ${pw}”`);
  if (!hits.length) return null;
  // Generator / attrs / class / font / id / powered-by / own-subdomain are strong;
  // a lone shared CDN host is weak.
  const strong = !!(subdomain || g || attr || data || cls || font || id || pw);
  return { fp, hits, strong, weight: hits.length + (strong ? 2 : 0) + (subdomain ? 2 : 0) };
}

/**
 * Analyse a site from collected DOM hints (+ optional content signals).
 * @returns {{platform,platformId,kind,aiBuilder,aiBuilt,verdict,signals,candidates}}
 */
export function siteForensics(hints = {}, content = {}) {
  const arr = (x) => (Array.isArray(x) ? x.map((s) => String(s).toLowerCase()) : []);
  const h = {
    generator: (hints.generator || '').toLowerCase(),
    hosts: arr(hints.hosts),
    paths: arr(hints.paths),
    attrs: arr(hints.attrs),
    dataAttrs: arr(hints.dataAttrs),
    classes: arr(hints.classes),
    fonts: (hints.fonts || '').toLowerCase(),
    ids: hints.ids || [],
    aiGradient: hints.aiGradient === true,
    powered: (hints.powered || '').toLowerCase(),
    hostname: (hints.hostname || '').toLowerCase(),
  };

  const ownHost = matchDefaultSubdomain(h.hostname);
  const kindRank = { 'ai-builder': 4, builder: 3, ecommerce: 2, cms: 1, docs: 0, framework: 0 };
  const scored = [];
  for (const fp of FINGERPRINTS) { const s = scoreFingerprint(fp, h, ownHost && ownHost.id); if (s) scored.push(s); }
  scored.sort((a, b) =>
    (kindRank[b.fp.kind] - kindRank[a.fp.kind]) || (b.weight - a.weight) ||
    (WEIGHTS.kind[b.fp.kind] - WEIGHTS.kind[a.fp.kind]));

  const primary = scored[0] || null;
  const signals = [];
  let codeProb = WEIGHTS.handcoded;
  if (primary) {
    codeProb = WEIGHTS.kind[primary.fp.kind] ?? 0.3;
    signals.push({ code: 'platform', name: primary.fp.name, kind: primary.fp.kind, hits: primary.hits.slice(0, 3) });
  } else {
    signals.push({ code: 'handcoded' });
  }

  // Heuristic code smells (noisy-OR combine so several weak signals add up).
  let aiBuilt = codeProb;
  for (const heur of HEURISTICS) {
    let ok = false; try { ok = heur.test(h); } catch { ok = false; }
    if (!ok) continue;
    aiBuilt = aiBuilt + (1 - aiBuilt) * heur.weight;
    signals.push({ code: 'heuristic', id: heur.id, label: heur.label });
  }

  // Generic vibe-coding deploy host — only when no builder platform was named
  // (a builder already tells us more). Weak on its own; matters when it stacks
  // with a code smell above (e.g. a shadcn app deployed to netlify.app).
  const primaryKind = primary ? primary.fp.kind : null;
  if (primaryKind !== 'ai-builder' && primaryKind !== 'builder') {
    const vibe = matchVibeHost(h.hostname);
    if (vibe) {
      aiBuilt = aiBuilt + (1 - aiBuilt) * WEIGHTS.vibeHost;
      signals.push({ code: 'vibeHost', host: vibe });
    }
  }

  // Content evidence.
  const images = content.images || 0;
  const aiImages = content.aiImages || 0;
  const imgShare = images ? aiImages / images : 0;
  const textShare = clamp01(content.aiTextShare || 0);
  if (aiImages > 0) signals.push({ code: 'aiImages', aiImages, images });
  if (content.textBlocks) signals.push({ code: 'aiText', aiBlocks: Math.round(textShare * content.textBlocks), textBlocks: content.textBlocks });

  aiBuilt = Math.max(aiBuilt, imgShare * WEIGHTS.imageEvidence);
  aiBuilt = clamp01(aiBuilt + WEIGHTS.textNudge * textShare + WEIGHTS.imageNudge * imgShare);

  const verdict = aiBuilt >= 0.6 ? 'ai' : aiBuilt >= 0.35 ? 'mixed' : 'human';

  return {
    platform: primary ? primary.fp.name : null,
    platformId: primary ? primary.fp.id : null,
    kind: primary ? primary.fp.kind : 'unknown',
    aiBuilder: !!(primary && primary.fp.kind === 'ai-builder'),
    aiBuilt,
    verdict,
    signals,
    candidates: scored.slice(0, 5).map((s) => ({ id: s.fp.id, name: s.fp.name, kind: s.fp.kind, hits: s.hits })),
  };
}

