/**
 * Site forensics — "was this website built with AI / a builder?"
 *
 * Pure, dependency-free. Fed lightweight DOM hints collected by the content
 * script (meta generator, asset hosts, root attributes/ids, data-* attribute
 * names, class tokens, body font, the page's gradient palette, footer text)
 * plus optional content signals (AI-text share, AI-watermarked images).
 *
 * Two layers of evidence:
 *   1. FINGERPRINTS — a named platform/builder left a trace (Framer, Wix,
 *      Webflow, Durable, Elementor…). data-* attributes and class tokens catch
 *      builders even on custom domains where the CDN host is hidden.
 *   2. HEURISTICS — code smells that strongly correlate with AI generation but
 *      aren't a single product: the shadcn/Radix/Lucide stack that v0 / Lovable
 *      / Bolt / Cursor emit, the Geist font, and the near-universal AI-startup
 *      violet→cyan gradient palette.
 *
 * WEIGHTS is the single tuning surface — a small "model" you can hand-tune now
 * and later fit to a labelled dataset (see docs at the bottom of the file).
 *
 * @module engine/site-forensics
 */

